function MS=Chan_3BS(BS,ri_1)
x1=BS(1,1);
y1=BS(1,2);
x2=BS(2,1);
y2=BS(2,2);
x3=BS(3,1);
y3=BS(3,2);
x2_1=x2-x1;
x3_1=x3-x1;
y2_1=y2-y1;
y3_1=y3-y1;
r2_1 =ri_1(1);
r3_1 =ri_1(2);
k1=x1.^2+y1.^2;
k2=x2.^2+y2.^2;
k3=x3.^2+y3.^2;
%X=[m;n];%´ýÇó¾ØÕó
X1=[x1;y1];
T=[x2_1,y2_1;x3_1,y3_1];
P1=-inv(T);
P2=[r2_1;r3_1];
P3=0.5*[-k2+k1+r2_1^2;-k3+k1+r3_1^2];
a=(P1*P2)'*(P1*P2)-1;
b=(P1*P2)'*(P1*P3-X1)+(P1*P3-X1)'*(P1*P2);
c=(P1*P3-X1)'*(P1*P3-X1);
R10=(-b+sqrt(b^2-4*a*c))/(2*a);
R11=(-b-sqrt(b^2-4*a*c))/(2*a);

X10=P1*P2*R10+P1*P3;
X11=P1*P2*R11+P1*P3;
MS=[X10';X11'];
end